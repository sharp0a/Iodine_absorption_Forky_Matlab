function [factor, ljfmax] = load_fcf(fcf_file)
    % Load Franck-Condon factors from fcfioded
    % Format: "  VP= 0   VS= 0   J=  0   T=15724.59   FCF=.1676-008   RC= 2.8265"
    fid = fopen(fcf_file, 'r');
    if fid == -1, error('Cannot open %s', fcf_file); end
    
    % ljfmax per FORTRAN lines 225-261
    ljfmax = zeros(1,70);
    ljfmax(1:25) = 8;
    ljfmax(26:46) = 7;
    ljfmax(47:52) = 6;
    ljfmax(53) = 5; ljfmax(54) = 6; ljfmax(55) = 6; 
    ljfmax(56) = 5; ljfmax(57) = 6; ljfmax(58) = 5;
    ljfmax(59:61) = 5;
    ljfmax(62:64) = 4; ljfmax(65) = 3; ljfmax(66) = 4;
    ljfmax(67:69) = 3; ljfmax(70) = 2;
    
    factor = zeros(70, 20, 9);
    
    for ivl = 0:19
        for ivu = 0:69
            max_lj = ljfmax(ivu+1);
            for lj = 0:max_lj
                line = fgetl(fid);
                if ischar(line) && length(line) >= 52
                    % FCF format: columns 44-52 contain ".XXXX-YYY"
                    fcf_str = line(44:52);
                    dash_pos = strfind(fcf_str, '-');
                    if ~isempty(dash_pos)
                        mantissa = str2double(fcf_str(1:dash_pos(1)-1));
                        exponent = str2double(fcf_str(dash_pos(1)+1:end));
                        factor(ivu+1, ivl+1, lj+1) = mantissa * 10^(-exponent);
                    else
                        factor(ivu+1, ivl+1, lj+1) = 0;
                    end
                else
                    factor(ivu+1, ivl+1, lj+1) = 0;
                end
            end
            fgetl(fid);  % Skip blank line
        end
        fgetl(fid);  % Skip blank line
    end
    fclose(fid);
end

function fcf = calc_fcf(ivu, ivl, j, factor, ljfmax)
    % Interpolate/extrapolate FCF (same logic as FORTRAN)
    if j >= 25*ljfmax(ivu+1)
        % Extrapolate
        lj1 = ljfmax(ivu+1) - 1;
        lj2 = ljfmax(ivu+1);
        rj1 = 25 * lj1;
        rj2 = 25 * lj2;
        fcf = factor(ivu+1, ivl+1, lj2+1) + ...
              (factor(ivu+1,ivl+1,lj2+1) - factor(ivu+1,ivl+1,lj1+1)) / ...
              (rj2*(rj2+1) - rj1*(rj1+1)) * (j*(j+1) - rj2*(rj2+1));
        if fcf <= 0
            fcf = factor(ivu+1, ivl+1, lj2+1);
        end
    else
        % Interpolate
        lj1 = floor(j/25);
        lj2 = lj1 + 1;
        rj1 = 25 * lj1;
        rj2 = 25 * lj2;
        fcf = factor(ivu+1, ivl+1, lj1+1) + ...
              (factor(ivu+1,ivl+1,lj2+1) - factor(ivu+1,ivl+1,lj1+1)) / ...
              (rj2*(rj2+1) - rj1*(rj1+1)) * (j*(j+1) - rj1*(rj1+1));
    end
end